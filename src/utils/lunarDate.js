// 简单的农历日期计算示例
const lunarMonths = [
    '正月', '二月', '三月', '四月', '五月', '六月',
    '七月', '八月', '九月', '十月', '冬月', '腊月'
  ];
  
  const lunarDays = [
    '初一', '初二', '初三', '初四', '初五', '初六', '初七', '初八', '初九', '初十',
    '十一', '十二', '十三', '十四', '十五', '十六', '十七', '十八', '十九', '二十',
    '廿一', '廿二', '廿三', '廿四', '廿五', '廿六', '廿七', '廿八', '廿九', '三十'
  ];
  
  export function getLunarDate(date) {
    // 这里使用一个简单的算法来模拟农历日期
    // 实际上，农历计算非常复杂，建议使用专业库
    const month = (date.getMonth() + 1) % 12; // 简单模拟月份
    const day = date.getDate() % 30; // 简单模拟日期
  
    return `${lunarMonths[month]}${lunarDays[day]}`;
  }